    private void SubmitjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitjButtonActionPerformed

        
        TechnicalpersonworkRequest request = new TechnicalpersonworkRequest();
        request.setComponent(componentjTextField.getText());
        request.setIssue(issuejTextField.getText());
        request.setStatus("waiting");
        request.setSender(userAccount);
        request.setSubmitdate(new Date());
        request.setRequestDate(new Date());
        request.setResolution("waiting");
                
        
        
        Organization org = null;
        for(Network network : system.getNetworkList())
            for(Enterprise ent : network.getEnterpriseDirectory().getEnterpriseList())
                    if(ent.getEnterpriseType().toString().equals("TechSupport"))
                        for (Organization organization : ent.getOrganizationDirectory().getOrganizationList()){
//                            if(organization == null){
//                                JOptionPane.showMessageDialog(this, "No organization. Please create an organization first","Error",JOptionPane.ERROR_MESSAGE);
//                            }
                                if(organization instanceof TechnicalPersonOrganization){
                                    org = organization;
                                    break;
                                }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }
        populatetable();
        
    }//GEN-LAST:event_SubmitjButtonActionPerformed

