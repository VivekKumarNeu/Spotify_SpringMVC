    public void populatetable(){
    
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.setRowCount(0);
            for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){    
            Object[] row = new Object[7];
            if(request instanceof TechnicalpersonworkRequest){
            row[0] = request.getRequestDate();
            row[1] = request.getReceiver();
            row[2] = request.getSender();
            row[3] = ((TechnicalpersonworkRequest) request).getIssue();
            row[4] = ((TechnicalpersonworkRequest) request).getComponent();
            row[5] = ((TechnicalpersonworkRequest) request).getResolution();
            row[6] = request.getStatus();
            
            
            model.addRow(row);
            }
        }
            
        jTable1.setSelectionBackground(Color.getHSBColor(0.2F, 0.1F, 0.2F));
        jTable1.setSelectionForeground(Color.white);
        }

    
    
    
