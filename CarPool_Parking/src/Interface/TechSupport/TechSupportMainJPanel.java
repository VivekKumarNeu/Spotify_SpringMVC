    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int selectedRow = jTable1.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a row.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        WorkRequest request = (WorkRequest)jTable1.getValueAt(selectedRow, 2);
        
        if(request.getStatus().equals("Fixed"))
        {
            JOptionPane.showMessageDialog(this, "Issue is already solved","",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        request.setReceiver(account);
        ((TechnicalpersonworkRequest) request).setResolution(resolutionjTextField.getText());
        request.setStatus("Fixed");
        populatetable();
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


