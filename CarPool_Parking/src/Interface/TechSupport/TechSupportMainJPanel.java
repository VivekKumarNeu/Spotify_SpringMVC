    public void populatetable()
    {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()){    
            Object[] row = new Object[6];
            row[0] = request.getReceiver();
            row[1] = ((TechnicalpersonworkRequest) request).getComponent();
            row[2] = ((TechnicalpersonworkRequest) request);
            row[3] = request.getStatus();
                   
            row[4] = request.getSender();
            row[5] = ((TechnicalpersonworkRequest) request).getResolution();
            model.addRow(row);
        }
        jTable1.setSelectionBackground(Color.getHSBColor(0.2F, 0.1F, 0.2F));
        jTable1.setSelectionForeground(Color.white);
        
    }
    
    
    
    
