    public void populatetable()
    {
        DefaultTableModel model = (DefaultTableModel) ratingjTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : useraccount.getWorkQueue().getWorkRequestList()){
          if(request instanceof CarOwnerWorkRequest){
                if(request.getReceiver() == null)
                    //JOptionPane.showMessageDialog(this, "No user found. Wait for trip to get over.");
                {} else{
            Object[] row = new Object[5];
            row[0] = request.getReceiver();
            row[1] = request;
            row[2] = request.getSource();
            row[3] = request.getDestination();
            row[4] = request.getRequestDate();
            model.addRow(row);
            }
            }
        }
        /****/
        for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList())
            for(WorkRequest workreq :  org.getWorkQueue().getWorkRequestList())
                if(workreq instanceof CarUserWorkRequest)
                    if(workreq.getReceiver() == null)
                    {}else{
                    //if(workreq.getReceiver().equals(useraccount))
                    if(((CarUserWorkRequest) workreq).getUserlist().contains(useraccount))
                        {
                    Object[] row = new Object[5];
                     row[0] = workreq.getSender();
                     row[1] = workreq;
                     row[2] = workreq.getSource();
                     row[3] = workreq.getDestination();
                     row[4] = workreq.getRequestDate();
                     model.addRow(row);
                    }
                    }
        ratingjTable.setSelectionBackground(Color.getHSBColor(0.2F, 0.1F, 0.2F));
        ratingjTable.setSelectionForeground(Color.white);
    }
    
