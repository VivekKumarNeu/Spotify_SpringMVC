    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        flag = true;
        int selectedRow = ratingjTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a row.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(rateme == null){
            JOptionPane.showMessageDialog(this, "Please select a rating.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
            
        WorkRequest request = (WorkRequest)ratingjTable.getValueAt(selectedRow, 1);
        UserAccount user=null;
        UserAccount user1=null;
        // sending from user to owner
        if(request instanceof CarOwnerWorkRequest){
                user = request.getReceiver();  // user is ownername
                user1 = request.getSender();   //  user1 is username
        }
        // sending from owner to user
        if(request instanceof CarUserWorkRequest){
            user = request.getSender();   //  user is ownername
            user1 = request.getReceiver(); // user1 is username

        }
        /******/
        for(Organization org :enterprise.getOrganizationDirectory().getOrganizationList())
        {if(org instanceof ApplicationAdminOrganization)
            {
                for(WorkRequest wr : org.getWorkQueue().getWorkRequestList())
                    if((wr.getSender().equals(user1)) && (wr.getReceiver().equals(user)))
                    {
                        JOptionPane.showMessageDialog(this, "Already rating is published");
                        flag = false;
                        break;
                    }
            }
        }   
            
        /****/
            if(flag)
            {
        WebsiteAdminWorkRequest req = new WebsiteAdminWorkRequest();
        req.setReceiver(user); // receiver is the owner
        req.setRating(rateme);
        req.setSender(useraccount); // sender is the user sending
        req.setRequestDate(new Date());
        ((WebsiteAdminWorkRequest) req).setMarkit(true);
        ((WebsiteAdminWorkRequest) req).setStatus("Waiting");
        
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof ApplicationAdminOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(req);
            useraccount.getWorkQueue().getWorkRequestList().add(req);
        }
        
        JOptionPane.showMessageDialog(this, "Rating has been sent to administrator. Thank You.","Information",JOptionPane.INFORMATION_MESSAGE);
        flag = true;
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

