    private void dollar10offerjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dollar10offerjButtonActionPerformed
        UserAccount userB=null;
        UserAccount userA=null;
        int rowowner  = CarownerjTable.getSelectedRow();
        int rowuser = CaruserrjTable.getSelectedRow();
        if(rowowner>=0 && rowuser >=0)
        {
            JOptionPane.showMessageDialog(this, "Please select only one row");
            popuatecarusertable();
            populatecarownertable();
           return;
        }
        if(rowowner!=-1)
        userA = (UserAccount)CarownerjTable.getValueAt(rowowner, 0);
        if(rowuser!=-1)
            userB = (UserAccount)CaruserrjTable.getValueAt(rowuser, 0);
        String org;
        
        if((rowowner < 0) && (rowuser <0) )
        {
            JOptionPane.showMessageDialog(this, "Please select a row","Error",JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            if(rowowner>=0)
                org = "Car Owner Organization";
            else
                org = "Caruser Organization";
            
            for (Organization org1 : enterprise.getOrganizationDirectory().getOrganizationList() ){
                if(org1.getName().equals(org))
                {
                    for(UserAccount ua : org1.getUserAccountDirectory().getUserAccountList()){
                            if(ua.equals(userA)){
                                //ua.getPerson().setAmount(ua.getPerson().getAmount() + 10);
                                ua.setPayment(ua.getPayment()+10);
                                break;}
                            if(ua.equals(userB)){
                              //  ua.getPerson().setAmount(ua.getPerson().getAmount() + 10);
                                ua.setPayment(ua.getPayment()+10);
                                break;}
                    }
                }
            }
        }
        popuatecarusertable();
        populatecarownertable();
    }//GEN-LAST:event_dollar10offerjButtonActionPerformed

