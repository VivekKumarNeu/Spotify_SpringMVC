    public void popuatecarusertable()
    {
        
        DefaultTableModel model = (DefaultTableModel) CarownerjTable.getModel();
        
        model.setRowCount(0);
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList() ){
                if(org.getName().equals("Car Owner Organization"))
                {
                    for(UserAccount ua : org.getUserAccountDirectory().getUserAccountList()){
                
                            Object[] row = new Object[5];
                            row[0] = ua;
                            row[1] = ua.getPerson().getPersonName();
                            row[2] = ua.getPerson().getPhonenumber();
                            row[3] = ua.getPayment();
                            row[4] = ua.getTrips();
                            model.addRow(row);
                    }          
                 }
         }
        
        CarownerjTable.setSelectionBackground(Color.getHSBColor(0.2F, 0.1F, 0.2F));
        CarownerjTable.setSelectionForeground(Color.white);
        
    }
    
